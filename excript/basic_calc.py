# Tipos de operações matemáticas em Python3

num1 = float(input("Digite um número: \n"))
num2 = float(input("Digite outro número: \n"))

sum = num1 + num2
sub = num1 - num2
multi = num1 * num2
div1 = num1 / num2
div2 = num1 // num2
resto = num1 % num2

print("Soma: %.2f" %sum)
print("Subtração: %.2f" %sub)
print("Multiplicação: %.2f" %multi)
print("Divisão real: %.2f" %div1)
print("Divisão inteira: %d" %div2)
print("Resto da divisão: %.2f" %resto)
